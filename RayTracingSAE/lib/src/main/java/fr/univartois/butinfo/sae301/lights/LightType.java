package fr.univartois.butinfo.sae301.lights;

/**
 * The `LightType` enum defines two types of light sources that can be used in a 3D scene.
 *
 * @author Theo, Louis, Hugo, Alex
 */
public enum LightType {
    /**
     * Represents a directional light source that emits light in a specific direction.
     */
    DIRECTIONAL,

    /**
     * Represents a point light source that emits light from a specific point in space.
     */
    POINT
}
